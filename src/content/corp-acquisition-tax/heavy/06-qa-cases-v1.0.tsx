      <p>#### 시행령 제26조제4항제2호의 임대가 불가피하다고 판단되는 업종에 포함되므로 판매시설의 용도로 임대하였을 경우(대규모점포.소규모점포 모두 포함)에도 직접사용하는 것으로 보아 중과하지 않음. (대법원2019두39918.2019.9.10.)</p>

