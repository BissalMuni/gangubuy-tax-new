      <p>2. 당사자의 신청에 의한 임차권설정등기의 경우와 임차권등기명령에 의한 주택임차권등기의 경우 차임(매월 임대차지급액)이 있는 경우에는 지방세법 제131조 제1항 제6호 (5)의 세율을 적용하고, 차임이 없는 경우와 차임이 있는 경우로서 지방세법 제131조 제1항 제6호 (5)의 세율을 적용한 세액이 3,000원 미만일 경우에는 3,000원의 세율을 적용하는 것(대법원 임차권등기에 관한 업무처리지침 2002.11.1. 등기예규 제1059호 참조)임</p>



