      <p>2. 연구개발 및 인력개발을 위한 비용으로서 「조세특례제한법 시행령」 별표 6의 비용이 해당 과세연도의 수입금액의 100분의 5(「벤처기업육성에 관한 특별조치법」 제25조에 따라 벤처기업 해당 여부에 대한 확인을 받은 날이 속하는 과세연도부터 연구개발 및 인력개발을 위한 비용의 비율이 100분의 5 이상을 유지하는 경우로 한정한다) 이상인 중소기업</p>



