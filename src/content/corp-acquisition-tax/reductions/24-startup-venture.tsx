      <p>⑥ 법 제58조의3제4항제5호 각 목 외의 부분에서 “대통령령으로 정하는 엔지니어링사업”이란 「조세특례제한법 시행령」 제5조제9항에 따른 엔지니어링사업을 말한다.</p>



