      <p>∙ 기업부설연구소의 사용공간이 독립된 건물, 빌딩의 1개 층 전체 또는 별도의 호실 등과 같이 다른 공간과 완전히 구분되어 있을 경우 그 전체를 연구공간으로 인정</p>

