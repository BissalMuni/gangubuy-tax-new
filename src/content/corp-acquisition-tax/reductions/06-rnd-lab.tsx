      <p>2. 「기업부설연구소등의 연구개발 지원에 관한 법률」 제7조제1항에 따라 기업부설연구소로 인정받은 날부터 3년 이내에 「조세특례제한법 시행령」 제9조제15항에 따른 연구개발세액공제기술심의위원회로부터 해당 기업부설연구소의 연구개발 대상 기술이 같은 영 별표 7의2에 따른 기술에 해당된다는 심의 결과를 통지받은 기업일 것</p>

