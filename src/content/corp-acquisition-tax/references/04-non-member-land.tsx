      <p>여기서, ‘부동산 취득’이란 부동산 취득자가 실질적으로 완전한 내용의 소유권을 취득하는지 여부와 관계없이 소유권이전의 형식에 의한 부동산취득의 모든 경우를 포함한다고 할 것(대법원 2005두9491, 2007. 4.12.)이나, 소유권이전등기의 원인이었던 계약을 소급적으로 실효시키는 합의해제 약정에 기초하여 소유권이전등기를 말소하는 원상회복조치의 결과로 그 소유권을 취득한 것은「지방세법」제6조제1호의 취득세 과세대상이 되는 부동산취득에 해당하지 않으며(대법원 93누11319, 1993.9.1., 조심2014지930, 2014.10.6. 등), 합의해제가 계약의 소급적 소멸을 목적으로 했다면 그 합의해제로 인하여 매수인 앞으로 이전되었던 부동산의 소유권이 당연히 매도인에게 복귀되는 것이므로 매도인이 원상회복의 방법으로 소유권이전등기를 했다고 하더라도 부동산 취득에 해당하지 않는다(대법원 85누1008, 1986. 3.25.)는 등의 판시내용을 감안할 때, 이미 자기 앞으로 소유권을 표상하는 등기가 되어 있었던 자가 원인무효 등기의 외관을 제거하고 소유권을 원상회복할 경우 취득세 과세대상 부동산을 취득한 것으로 볼 수 없다고 할것입니다.</p>



